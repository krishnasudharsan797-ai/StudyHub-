export const searchAcrossData = (query) => {
  const lower = query.toLowerCase();
  const results = { domains: [], skills: [], platforms: [] };

  domainsData.forEach(domain => {
    if (domain.name.toLowerCase().includes(lower)) {
      results.domains.push(domain);
    }

    domain.skills.forEach(skill => {
      if (skill.name.toLowerCase().includes(lower)) {
        results.skills.push({ ...skill, domain: domain.name });
      }

      skill.platforms.forEach(platform => {
        if (platform.name.toLowerCase().includes(lower)) {
          results.platforms.push({
            ...platform,
            skill: skill.name,
            domain: domain.name
          });
        }
      });
    });
  });

  return results;
};